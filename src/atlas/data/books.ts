import type { BookMeta, YearRange } from '../types';

/**
 * The canon, each book tied to the historical window its narrative occupies.
 *
 * Note the distinction the type enforces: `narrativeRange` is when the action is
 * set, not when the book was written. Deuteronomy is set in a few weeks on the
 * plains of Moab; its composition history is a separate question the atlas does
 * not take a position on. For books with no narrative setting at all — most of the
 * wisdom literature — the range is the period the material is traditionally
 * associated with, and the UI labels it as an association rather than a setting.
 *
 * `placesByChapter` is a working index, not a concordance. Books with
 * `indexed: false` show the places we have confirmed for them; the interface says
 * so plainly rather than implying the list is complete.
 */

interface BookInput {
  id: string;
  name: string;
  testament: 'old' | 'new';
  division: string;
  chapters: number;
  narrativeRange: YearRange;
  periods: string[];
  placesByChapter?: Record<number, string[]>;
  indexed?: boolean;
}

function book(input: BookInput): BookMeta {
  return {
    ...input,
    placesByChapter: input.placesByChapter ?? {},
    indexed: input.indexed ?? false,
  };
}

